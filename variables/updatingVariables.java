package variables;

public class updatingVariables {
    public static void main(String[] args) {
        int peoples = 5;
        System.out.println(peoples);

        peoples = peoples + 1;
        System.out.println(peoples);

        peoples += 1;
        System.out.println(peoples);

        peoples = peoples - 1;
        System.out.println(peoples);

        peoples -= 1;
        System.out.println(peoples);
    }
}

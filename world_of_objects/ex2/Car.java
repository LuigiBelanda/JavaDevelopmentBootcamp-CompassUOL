package world_of_objects.ex2;

public class Car {
    // Atributos
    String make;
    double price;
    int year;
    String color;

    // Construtor
    public Car (String make, double price, int year, String color) {
        this.make = make;
        this.price = price;
        this.year = year;
        this.color = color;
    }
}
